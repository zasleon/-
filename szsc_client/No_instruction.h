char* search_card(int situation,int number)                     //查询编号代表的手卡效果
{

	switch(situation)
	{
		case p_name:
			if(catalog[number][0]!=NULL)return catalog[number][0];
			break;
		case p_details:
			if(catalog[number][1]!=NULL)return catalog[number][1];
			break;
	}
	cout<<"出错！number="<<number<<endl;

	return "莫名其妙号卡";
}



